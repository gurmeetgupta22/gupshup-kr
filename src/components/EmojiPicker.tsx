const RECENT_STORAGE_KEY = 'vibecheck_recent_emojis';
const MAX_RECENT = 24;

function getRecentEmojis(): string[] {
  if (typeof window === 'undefined') return [];
  try {
    const stored = localStorage.getItem(RECENT_STORAGE_KEY);
    return stored ? JSON.parse(stored) : [];
  } catch {
    return [];
  }
}

function saveRecentEmoji(emoji: string) {
  if (typeof window === 'undefined') return;
  try {
    const recent = getRecentEmojis();
    const filtered = recent.filter(e => e !== emoji);
    const updated = [emoji, ...filtered].slice(0, MAX_RECENT);
    localStorage.setItem(RECENT_STORAGE_KEY, JSON.stringify(updated));
  } catch {}
}

interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
  onClose: () => void;
  position?: 'top' | 'bottom';
}

export function EmojiPicker({ onSelect, onClose, position = 'top' }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState<string>('smileys');
  const [recentEmojis, setRecentEmojis] = useState<string[]>([]);
  const pickerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    setRecentEmojis(getRecentEmojis());
  }, []);

  useEffect(() => {
    const handleClickOutside = (e: MouseEvent) => {
      if (pickerRef.current && !pickerRef.current.contains(e.target as Node)) {
        onClose();
      }
    };
    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [onClose]);

  const handleEmojiClick = (emoji: string) => {
    saveRecentEmoji(emoji);
    setRecentEmojis(getRecentEmojis());
    onSelect(emoji);
  };

  const categories = Object.entries(EMOJI_CATEGORIES);
  const currentCategory = activeCategory === 'recent' 
    ? { ...EMOJI_CATEGORIES.recent, emojis: recentEmojis }
    : EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES];

  return (
    <motion.div
      ref={pickerRef}
      initial={{ opacity: 0, scale: 0.9, y: position === 'top' ? 10 : -10 }}
      animate={{ opacity: 1, scale: 1, y: 0 }}
      exit={{ opacity: 0, scale: 0.9, y: position === 'top' ? 10 : -10 }}
      className={`absolute ${position === 'top' ? 'bottom-full mb-2' : 'top-full mt-2'} left-0 z-50 w-[320px] md:w-[360px] bg-zinc-900 rounded-2xl border border-zinc-800 shadow-2xl overflow-hidden`}
    >
      <div className="flex items-center gap-1 p-2 border-b border-zinc-800 overflow-x-auto scrollbar-hide">
        {recentEmojis.length > 0 && (
          <button
            onClick={() => setActiveCategory('recent')}
            className={`p-2 rounded-lg transition-all flex-shrink-0 ${activeCategory === 'recent' ? 'bg-blue-600 text-white' : 'text-zinc-400 hover:bg-zinc-800'}`}
          >
            <Clock className="w-4 h-4" />
          </button>
        )}
        {categories.filter(([key]) => key !== 'recent').map(([key, category]) => {
          const Icon = category.icon;
          return (
            <button
              key={key}
              onClick={() => setActiveCategory(key)}
              className={`p-2 rounded-lg transition-all flex-shrink-0 ${activeCategory === key ? 'bg-blue-600 text-white' : 'text-zinc-400 hover:bg-zinc-800'}`}
            >
              <Icon className="w-4 h-4" />
            </button>
          );
        })}
      </div>

      <div className="p-2 max-h-[280px] overflow-y-auto">
        <p className="text-xs font-bold text-zinc-500 uppercase tracking-wider mb-2 px-1">
          {activeCategory === 'recent' ? 'Recently Used' : currentCategory.label}
        </p>
        
        {activeCategory === 'recent' && recentEmojis.length === 0 ? (
          <p className="text-zinc-500 text-sm text-center py-8">No recent emojis yet</p>
        ) : (
          <div className="grid grid-cols-8 gap-1">
            {(activeCategory === 'recent' ? recentEmojis : currentCategory.emojis).map((emoji, i) => (
              <motion.button
                key={`${emoji}-${i}`}
                onClick={() => handleEmojiClick(emoji)}
                className="text-xl p-1.5 rounded-lg hover:bg-zinc-800 transition-colors"
                whileHover={{ scale: 1.2 }}
                whileTap={{ scale: 0.9 }}
              >
                {emoji}
              </motion.button>
            ))}
          </div>
        )}
      </div>
    </motion.div>
  );
}
